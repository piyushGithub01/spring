package com.chatbox;

public class Message {
	
	private String textMessage;

	public Message() {
	}

	public Message(String textMessage) {
		this.textMessage = textMessage;
	}

	public String getTextMessage() {
		return textMessage;
	}

	public void setTextMessage(String text) {
		this.textMessage = text;
	}
	
	
}
